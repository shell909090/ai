"""Pressure test: repeated tool calls — does tool result accumulation accelerate breakdown?"""

from __future__ import annotations

import argparse
import asyncio
import logging
import sys
import time
from collections.abc import AsyncIterator
from dataclasses import dataclass
from pathlib import Path
from typing import Any

import yaml

sys.path.insert(0, str(Path(__file__).parent.parent))

from little_agent._utils import _deep_merge
from little_agent.agent.agent import AgentCore
from little_agent.agent.permissions import YesManChecker
from little_agent.agent.tool_manager import ToolManager
from little_agent.backends.build import _DEFAULT_BACKEND_CONFIG, _build_backend
from little_agent.tools.protocol import ToolArgDef, ToolDef
from little_agent.types import JSONValue, Session

TURN_TIMEOUT_S = 300
OUTPUT_TOKENS_ALARM = 20_000
MAX_TURNS = 60

# User prompts: ask about known system files the model cannot predict without reading
_PROMPTS = [
    "What does the file /etc/hosts contain? Use the read tool to check.",
    "Please read /etc/hostname and tell me its contents.",
    "Use the read tool to show me the contents of /etc/os-release.",
    "Read the file /etc/shells and report what shells are listed.",
    "What is in /etc/timezone? Use the available tool to find out.",
]

# Mock tool return content: two sizes
_MOCK_SMALL = "output: {n}"  # ~5 tokens

_MOCK_LARGE = (
    "# /etc/hosts or similar system file - entry #{n}\n"
    "127.0.0.1   localhost\n"
    "127.0.1.1   host-{n:03d}\n"
    "::1         localhost ip6-localhost\n"
    "# Additional entry {n}: 10.0.0.{n} internal-{n:03d}.local\n"
    "# Generated by network manager, entry {n}"
)  # ~80 tokens


# ---------------------------------------------------------------------------
# Mock read provider
# ---------------------------------------------------------------------------

class _MockReadProvider:
    """Mock read tool: records calls, returns preset file content."""

    def __init__(self, large: bool, call_count: list[int]) -> None:
        self._large = large
        self._call_count = call_count

    def __iter__(self):
        large = self._large
        call_count = self._call_count

        async def _fn(_args: Any, _session: Session) -> JSONValue:
            call_count[0] += 1
            n = call_count[0]
            content = _MOCK_LARGE.format(n=n) if large else _MOCK_SMALL.format(n=n)
            return {"status": "completed", "content": content}

        args = [ToolArgDef("path", "string", "Absolute path to the file", True)]
        yield "read", ToolDef(desc="Read the contents of a file", args=args), _fn


# ---------------------------------------------------------------------------
# Instrumented backend
# ---------------------------------------------------------------------------

class _InstrumentedBackend:
    """Proxy that captures per-call stats."""

    def __init__(self, inner: Any) -> None:
        self._inner = inner
        self.last: dict[str, Any] = {}

    @property
    def context_window(self) -> int:
        return self._inner.context_window

    async def generate(self, session: Any) -> AsyncIterator:
        async for item in self._inner.generate(session):
            if hasattr(item, "finish_reason") and hasattr(item, "output_text"):
                usage = getattr(item, "usage", None) or {}
                self.last = {
                    "input_tokens": usage.get("input_tokens"),
                    "output_tokens": usage.get("output_tokens"),
                    "text_len": len(item.output_text),
                }
            yield item


# ---------------------------------------------------------------------------
# Turn result
# ---------------------------------------------------------------------------

@dataclass
class TurnResult:
    """Stats for a single turn."""

    turn: int
    elapsed_s: float
    input_tokens: int | None
    output_tokens: int | None
    tool_calls_total: int
    status: str  # ok | timeout | runaway | error

    def failed(self) -> bool:
        return self.status != "ok"

    def __str__(self) -> str:  # noqa: D105
        it = self.input_tokens if self.input_tokens is not None else "?"
        ot = self.output_tokens if self.output_tokens is not None else "?"
        return (
            f"T{self.turn:03d}  in={it}  out={ot}"
            f"  elapsed={self.elapsed_s:.1f}s"
            f"  tool_calls_total={self.tool_calls_total}"
            f"  [{self.status}]"
        )


def _classify(elapsed_s: float, output_tokens: int | None) -> str:
    if elapsed_s >= TURN_TIMEOUT_S:
        return "timeout"
    if output_tokens is not None and output_tokens >= OUTPUT_TOKENS_ALARM:
        return "runaway"
    return "ok"


# ---------------------------------------------------------------------------
# Agent factory
# ---------------------------------------------------------------------------

class _NullClient:
    async def update(self, _s: Any, _u: Any) -> None:
        pass

    async def check_permission(self, _s: Any, _t: str, _a: Any) -> bool:
        return True


def _load_config(path: str) -> dict[str, Any]:
    with open(path, encoding="utf-8") as f:
        return yaml.safe_load(f)


def _build(
    config: dict[str, Any], large: bool, call_count: list[int]
) -> tuple[AgentCore, _InstrumentedBackend]:
    raw = _build_backend(
        _deep_merge(_DEFAULT_BACKEND_CONFIG, config.get("backends", {}).get("primary", {})),
        "primary",
    )
    backend = _InstrumentedBackend(raw)
    tools = ToolManager()
    tools.register(_MockReadProvider(large=large, call_count=call_count))
    agent = AgentCore(
        client=_NullClient(),
        backend=backend,
        tools=tools,
        permissions=YesManChecker(),
        ignore_agentsmd=True,
        max_tool_result_chars=5000,
    )
    return agent, backend


# ---------------------------------------------------------------------------
# Single variant runner
# ---------------------------------------------------------------------------

async def _run_variant(config: dict[str, Any], large: bool, max_turns: int) -> None:
    label = "T-large (~80 tok/call)" if large else "T-small (~10 tok/call)"
    print(f"\n{'='*60}")
    print(f"Variant {label} | timeout={TURN_TIMEOUT_S}s | alarm=out>={OUTPUT_TOKENS_ALARM}")
    print(f"{'='*60}")

    results: list[TurnResult] = []
    total_calls = 0

    for turn in range(1, max_turns + 1):
        msg = _PROMPTS[(turn - 1) % len(_PROMPTS)]
        # Create a fresh agent and session each turn to avoid history bias
        call_count_turn: list[int] = [0]
        agent_turn, backend_turn = _build(config, large=large, call_count=call_count_turn)
        session = await agent_turn.new()
        t0 = time.monotonic()

        try:
            await asyncio.wait_for(session.prompt(msg), timeout=float(TURN_TIMEOUT_S))
        except TimeoutError:
            elapsed = time.monotonic() - t0
            total_calls += call_count_turn[0]
            r = TurnResult(
                turn=turn, elapsed_s=elapsed,
                input_tokens=None, output_tokens=None,
                tool_calls_total=call_count_turn[0], status="timeout",
            )
            results.append(r)
            print(r)
            print(f"\n*** TIMEOUT at T{turn} after {elapsed:.0f}s — stopping ***")
            break
        except Exception as exc:
            elapsed = time.monotonic() - t0
            total_calls += call_count_turn[0]
            r = TurnResult(
                turn=turn, elapsed_s=elapsed,
                input_tokens=backend_turn.last.get("input_tokens"),
                output_tokens=backend_turn.last.get("output_tokens"),
                tool_calls_total=call_count_turn[0], status="error",
            )
            results.append(r)
            print(f"{r}  error={exc!r:.80}")
            print(f"\n*** ERROR at T{turn} — stopping ***")
            break

        elapsed = time.monotonic() - t0
        total_calls += call_count_turn[0]
        out_tok = backend_turn.last.get("output_tokens")
        in_tok = backend_turn.last.get("input_tokens")
        status = _classify(elapsed, out_tok)

        r = TurnResult(
            turn=turn, elapsed_s=elapsed,
            input_tokens=in_tok, output_tokens=out_tok,
            tool_calls_total=call_count_turn[0], status=status,
        )
        results.append(r)
        print(r)

        if r.failed():
            print(f"\n*** FAILURE at T{turn}: {status} — stopping ***")
            break

    # Summary
    ok = [r for r in results if not r.failed()]
    failed = [r for r in results if r.failed()]
    print(f"\n--- Summary (Variant {label}) ---")
    print(f"Completed turns:     {len(ok)}")
    print(f"Total tool calls:    {total_calls}")
    if failed:
        f = failed[0]
        print(f"First failure:       T{f.turn} ({f.status})")
        print(f"  input_tokens:      {f.input_tokens}")
        print(f"  output_tokens:     {f.output_tokens}")
        print(f"  tool_calls_total:  {f.tool_calls_total}")
        print(f"  elapsed:           {f.elapsed_s:.1f}s")
    else:
        last = results[-1] if results else None
        if last:
            print(f"No failure in {len(results)} turns")
            print(f"  Last input_tokens: {last.input_tokens}")


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

async def _main(args: argparse.Namespace) -> None:
    config = _load_config(args.config)
    for run_idx in range(1, args.runs + 1):
        if args.runs > 1:
            print(f"\n### Run {run_idx}/{args.runs} ###")
        if args.variant in ("small", "both"):
            await _run_variant(config, large=False, max_turns=args.max_turns)
        if args.variant in ("large", "both"):
            await _run_variant(config, large=True, max_turns=args.max_turns)


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Pressure test: repeated tool calls — does result accumulation accelerate?"
    )
    parser.add_argument(
        "--config", default=str(Path.home() / ".config/little_agent/config.yaml")
    )
    parser.add_argument(
        "--variant", choices=["small", "large", "both"], default="both",
        help="small=~10tok/call, large=~80tok/call, both=run sequentially",
    )
    parser.add_argument("--max-turns", type=int, default=MAX_TURNS)
    parser.add_argument("--runs", type=int, default=3, help="Number of independent runs")
    parser.add_argument("--loglevel", default="WARNING")
    args = parser.parse_args()
    logging.basicConfig(level=args.loglevel.upper())
    asyncio.run(_main(args))


if __name__ == "__main__":
    main()
